public class ColorGradinet {

    private final int color1;
    private final int color2;

    public ColorGradinet(int color1, int color2) {
        this.color1 = color1;
        this.color2 = color2;
    }

    public int getColor1() {
        return color1;
    }

    public int getColor2() {
        return color2;
    }

}
