package com.codescannerqr.generator.model;

public class ResultButtons {

    private String nameButton;
    private int logoButton;

    public ResultButtons(String nameButton, int logoButton) {
        this.nameButton = nameButton;
        this.logoButton = logoButton;
    }

    public String getNameButton() {
        return nameButton;
    }

    public int getLogoButton() {
        return logoButton;
    }

}
